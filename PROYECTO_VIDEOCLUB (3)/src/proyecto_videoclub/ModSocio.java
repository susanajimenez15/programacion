    private void modificarSocioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarSocioActionPerformed
        //modificar socio
        try{
            if(!"".equals(nombre.getText())){
                String Query = "update socios set nombre = '" +nombre.getText()+"' where nombre = " + nombre.getText()+";";
                Statement st = con.createStatement();
                st.executeUpdate(Query);
            }
            if(!"".equals(dni.getText())){
                String Query = "update socios set dni = '" +dni.getText()+"' where dni = " + dni.getText()+";";
                Statement st = con.createStatement();
                st.executeUpdate(Query);
            }
            if(!"".equals(telefono.getText())){

                String Query = "update socios set telefono = '" +telefono.getText()+"' where telefono = " + telefono.getText()+";";
                Statement st = con.createStatement();
                st.executeUpdate(Query);
            }

            if(!"".equals(email.getText())){
                String Query = "update socios set email = '" +email.getText()+"' where email = " + email.getText()+";";
                Statement st = con.createStatement();
                st.executeUpdate(Query);
            }

            JOptionPane.showMessageDialog(null," SE HA MODIFICADO LA INFORMACION ");

        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            Logger.getLogger(Socios.class.getName()).log(Level.SEVERE, null, ex);

        }
    }//GEN-LAST:event_modificarSocioActionPerformed

