    private void consultarSocioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarSocioActionPerformed
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Numero Socio");
        modelo.addColumn("Nombre");
        modelo.addColumn("Dni");
        modelo.addColumn("Telefono");
        modelo.addColumn("Email");

        try{
            Statement st = con.createStatement();
            ResultSet rs =  st.executeQuery("select * from socios where codigo_pelicula = "+numSocio.getText());
            while(rs.next()){
                Object[] datos = new Object[6];
                for(int i = 0; i < 6; i++){
                    datos[i] = rs.getObject(i+1);
                }
                modelo.addRow(datos);
            }
            jTable1.setModel(modelo);
        }catch(SQLException ex){
            Logger.getLogger(Socios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_consultarSocioActionPerformed

